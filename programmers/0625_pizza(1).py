"""피자 나눠 먹기 (1).

결과: 해결, 한 줄 공식 추가 학습
체감 난이도: 5/5
핵심: 나머지, 올림 나눗셈

처음 접근:
- 7로 나누어떨어지는 경우와 나머지가 있는 경우를 if/else로 나눴다.

원인과 해결:
- 나머지가 있으면 사람이 한 명만 남더라도 피자 한 판이 더 필요하다.
- 양의 정수에서는 (n + 6) // 7로 같은 계산을 한 줄로 표현할 수 있다.

다음에 떠올릴 신호:
- 일부가 남으면 묶음 하나가 더 필요한 문제에서는 올림 나눗셈을 생각한다.

관련 노트:
- notes/CeilingDevision.md
"""


def solution(n):
    return (n + 6) // 7
