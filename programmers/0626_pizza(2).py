"""피자 나눠 먹기 (2).

결과: 해결
체감 난이도: 4/5
핵심: 최소공배수

막힌 부분:
- 모든 사람이 같은 수의 조각을 먹고 남는 조각이 없어야 한다는 조건을 식으로 바꾸기 어려웠다.

원인과 해결:
- 총 조각 수는 한 판의 6조각과 사람 수 n의 공통 배수여야 한다.
- 가장 적은 피자를 구하므로 두 수의 최소공배수를 구한다.

다음에 떠올릴 신호:
- 두 조건을 동시에 만족하는 가장 작은 수가 필요하면 LCM을 생각한다.

관련 노트:
- notes/GCD_LCM.md
"""

import math


def solution(n):
    total_slices = math.lcm(n, 6)
    return total_slices // 6
