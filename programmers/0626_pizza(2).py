# 문제: 피자 나눠먹기 (2)
# 난이도: Lv.0 / 체감: ⭐⭐⭐⭐
# 핵심: 최소공배수(LCM), math.lcm, GCD
# 복습: 2026-06-30, 2026-07-02, 2026-07-06

import math

def solution(n):
    if n % 6 == 0:
        return n / 6 # 한 조각씩 먹으면 돼
    else:
        return math.lcm(n,6) // 6
        # return (n * 6) // math.gcd(n,6) // 6  
    
'''
"남는 조각 없이 모두 같은 수로 나눠야 한다"
1. 총 조각 수가 6의 배수여야 함 (피자 한 판이 6조각이니까)
2. 총 조각 수가 n의 배수여야 함 (n명이 똑같이 나눠야 하니까)

"두 조건을 동시에 만족하는 최솟값" -> 최소공배수
'''